# Роман заметил, что кассиры много времени тратят на подсчёт мелочи. Он решил, что теперь все суммы будут округляться до 0 знаков после запятой. Подрядчик уже реализовал работу кассы, она работает только с дробными числами(float). Теперь нужно добавить функционал округления.

